let current = 0;

function renderSlide(index) {
  const slide = slides[index];
  document.getElementById('slide-display').innerHTML = `
    <div class="slide-tag">${slide.tag}</div>
    <h2>${slide.title}</h2>
    ${slide.content}
  `;
  document.getElementById('current-num').textContent = index + 1;
  document.getElementById('btn-prev').disabled = index === 0;
  document.getElementById('btn-next').disabled = index === slides.length - 1;
  document.querySelectorAll('.slide-dot').forEach((d, i) => {
    d.classList.toggle('active', i === index);
  });
}

function nextSlide() {
  if (current < slides.length - 1) { current++; renderSlide(current); }
}

function prevSlide() {
  if (current > 0) { current--; renderSlide(current); }
}

function setMode(mode) {
  const isSlides = mode === 'slides';
  document.getElementById('slide-view').style.display = isSlides ? '' : 'none';
  document.getElementById('reader-view').style.display = isSlides ? 'none' : '';
  document.getElementById('btn-slides').classList.toggle('active', isSlides);
  document.getElementById('btn-reader').classList.toggle('active', !isSlides);
}

// Build dots
function buildDots() {
  const container = document.getElementById('dots');
  slides.forEach((_, i) => {
    const btn = document.createElement('button');
    btn.className = 'slide-dot' + (i === 0 ? ' active' : '');
    btn.setAttribute('aria-label', `Slide ${i + 1}`);
    btn.addEventListener('click', () => { current = i; renderSlide(current); });
    container.appendChild(btn);
  });
}

// Keyboard navigation
document.addEventListener('keydown', e => {
  if (document.getElementById('slide-view').style.display !== 'none') {
    if (e.key === 'ArrowRight') nextSlide();
    if (e.key === 'ArrowLeft') prevSlide();
  }
});

document.getElementById('total-num').textContent = slides.length;
buildDots();
renderSlide(0);
